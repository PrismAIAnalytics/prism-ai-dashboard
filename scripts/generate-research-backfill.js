#!/usr/bin/env node
/**
 * generate-research-backfill.js (T-113)
 *
 * Dev-time generator: parses the website repo's blog sources into a committed
 * JSON the dashboard can load WITHOUT the website repo (Railway can't read it).
 *
 * For every prism_website_project/blog-sources/*.md it emits one record per
 * (external URL, blog) citation event:
 *   { url, claim_supported, cited_in: "blog:<slug>", reason, pulled_at, tags }
 *
 * pulled_at = min(blog `date:`, today) — future-dated drafts clamp to today so
 * the trend timeline isn't polluted with "pulls" that haven't happened.
 *
 * Usage:
 *   node scripts/generate-research-backfill.js [--today=YYYY-MM-DD] [--out=path]
 *
 * Output (default): config/research-sources-backfill.json
 */
const fs = require('fs');
const path = require('path');

const args = process.argv.slice(2);
const getArg = (name, def) => {
  const hit = args.find((a) => a.startsWith(`--${name}=`));
  return hit ? hit.split('=').slice(1).join('=') : def;
};

const today = getArg('today', new Date().toISOString().slice(0, 10));
const BLOG_DIR = path.resolve(__dirname, '..', '..', '..', 'prism_website_project', 'blog-sources');
const OUT = path.resolve(__dirname, '..', getArg('out', path.join('config', 'research-sources-backfill.json')));

const EXTERNAL_SKIP = /prismaianalytics\.com|localhost|127\.0\.0\.1/i;

function parseFrontmatter(text) {
  const m = text.match(/^---\n([\s\S]*?)\n---/);
  const fm = {};
  if (!m) return fm;
  for (const line of m[1].split('\n')) {
    const kv = line.match(/^([a-zA-Z_]+):\s*(.*)$/);
    if (kv) fm[kv[1]] = kv[2].trim().replace(/^["']|["']$/g, '');
  }
  return fm;
}

// Strip markdown/HTML noise from anchor text → a clean claim phrase.
function cleanClaim(s) {
  return String(s)
    .replace(/\*\*?/g, '')
    .replace(/&mdash;/g, '—')
    .replace(/&[a-z]+;/g, ' ')
    .replace(/[`*_]/g, '')
    .replace(/\s+/g, ' ')
    .trim()
    .slice(0, 160);
}

function clampDate(d) {
  if (!/^\d{4}-\d{2}-\d{2}$/.test(d || '')) return today;
  return d > today ? today : d;
}

function main() {
  if (!fs.existsSync(BLOG_DIR)) {
    console.error(`Blog sources not found at ${BLOG_DIR} — run from a checkout with the website repo alongside the dashboard.`);
    process.exit(1);
  }
  const files = fs.readdirSync(BLOG_DIR).filter((f) => f.endsWith('.md'));
  const records = [];
  let skippedInternal = 0;

  for (const file of files) {
    const full = path.join(BLOG_DIR, file);
    const text = fs.readFileSync(full, 'utf8').replace(/\r\n/g, '\n'); // normalize CRLF (Windows)
    const fm = parseFrontmatter(text);
    const slug = fm.slug || file.replace(/\.md$/, '');
    const title = fm.title || slug;
    const pulledAt = clampDate(fm.date);
    const body = text.replace(/^---\n[\s\S]*?\n---/, '');
    const seen = new Set(); // dedup (url) within a single blog

    // Markdown links: [anchor](url) — anchor is the claim.
    const mdLink = /\[([^\]]+)\]\((https?:\/\/[^\s)]+)\)/g;
    let m;
    while ((m = mdLink.exec(body)) !== null) {
      const anchor = m[1];
      const url = m[2].replace(/[.,;)]+$/, '');
      if (EXTERNAL_SKIP.test(url)) { skippedInternal++; continue; }
      if (seen.has(url)) continue;
      seen.add(url);
      records.push({
        url,
        claim_supported: cleanClaim(anchor),
        cited_in: `blog:${slug}`,
        reason: `Cited in blog "${cleanClaim(title)}"`,
        pulled_at: pulledAt,
        tags: 'backfill',
      });
    }

    // Bare URLs not already captured as markdown links — claim = its line.
    const bare = /(^|[^(])\b(https?:\/\/[^\s)<>"']+)/g;
    while ((m = bare.exec(body)) !== null) {
      const url = m[2].replace(/[.,;)]+$/, '');
      if (EXTERNAL_SKIP.test(url)) { skippedInternal++; continue; }
      if (seen.has(url)) continue;
      seen.add(url);
      const lineStart = body.lastIndexOf('\n', m.index) + 1;
      let lineEnd = body.indexOf('\n', m.index);
      if (lineEnd === -1) lineEnd = body.length;
      const claim = cleanClaim(body.slice(lineStart, lineEnd).replace(url, ''));
      records.push({
        url,
        claim_supported: claim || null,
        cited_in: `blog:${slug}`,
        reason: `Cited in blog "${cleanClaim(title)}"`,
        pulled_at: pulledAt,
        tags: 'backfill',
      });
    }
  }

  // Stable sort: by pulled_at then cited_in then url, for a deterministic diff.
  records.sort((a, b) =>
    a.pulled_at.localeCompare(b.pulled_at) ||
    a.cited_in.localeCompare(b.cited_in) ||
    a.url.localeCompare(b.url));

  const payload = {
    _comment: 'Generated by scripts/generate-research-backfill.js from prism_website_project/blog-sources. Loaded into the ledger by scripts/backfill-research-sources.js (idempotent). Do not hand-edit.',
    generated_for_today: today,
    count: records.length,
    records,
  };
  fs.mkdirSync(path.dirname(OUT), { recursive: true });
  fs.writeFileSync(OUT, JSON.stringify(payload, null, 2) + '\n');

  const distinctUrls = new Set(records.map((r) => r.url)).size;
  const distinctBlogs = new Set(records.map((r) => r.cited_in)).size;
  console.log(`Wrote ${records.length} citation events (${distinctUrls} distinct URLs across ${distinctBlogs} blogs) → ${path.relative(process.cwd(), OUT)}`);
  console.log(`Skipped ${skippedInternal} internal/self links. Dates clamped at ${today}.`);
}

main();
